function [Fs,descri] = sevenPointAlgorithmSelf(x)
Q1 = x(:,:,1);
Q2 = x(:,:,2);

Q = [Q2(:,1).*Q1(:,1),...
    Q2(:,1).*Q1(:,2),...
    Q2(:,1).*Q1(:,3),...
    Q2(:,2).*Q1(:,1),...
    Q2(:,2).*Q1(:,2),...
    Q2(:,2).*Q1(:,3),...
    Q2(:,3).*Q1(:,1),...
    Q2(:,3).*Q1(:,2),...
    Q2(:,3).*Q1(:,3),...
    ];

[~, ~, V] = svd(Q);
EE = V(:,8:9);
F1 = reshape(EE(:,1), [3,3])';
F2 = reshape(EE(:,2), [3,3])';

e1_1_1 = F1(1,1);
e1_1_2 = F1(1,2);
e1_1_3 = F1(1,3);
e1_2_1 = F1(2,1);
e1_2_2 = F1(2,2);
e1_2_3 = F1(2,3);
e1_3_1 = F1(3,1);
e1_3_2 = F1(3,2);
e1_3_3 = F1(3,3);
e2_1_1 = F2(1,1);
e2_1_2 = F2(1,2);
e2_1_3 = F2(1,3);
e2_2_1 = F2(2,1);
e2_2_2 = F2(2,2);
e2_2_3 = F2(2,3);
e2_3_1 = F2(3,1);
e2_3_2 = F2(3,2);
e2_3_3 = F2(3,3);

AA = e1_1_1*e1_2_2*e1_3_3 - e1_1_1*e1_2_3*e1_3_2 - e1_1_2*e1_2_1*e1_3_3 + e1_1_2*e1_2_3*e1_3_1 + e1_1_3*e1_2_1*e1_3_2 - e1_1_3*e1_2_2*e1_3_1;
BB = e1_1_1*e1_2_2*e2_3_3 - e1_1_1*e1_2_3*e2_3_2 - e1_1_1*e1_3_2*e2_2_3 + e1_1_1*e1_3_3*e2_2_2 -e1_1_2*e1_2_1*e2_3_3 + e1_1_2*e1_2_3*e2_3_1 + e1_1_2*e1_3_1*e2_2_3 - e1_1_2*e1_3_3*e2_2_1 +e1_1_3*e1_2_1*e2_3_2 - e1_1_3*e1_2_2*e2_3_1 - e1_1_3*e1_3_1*e2_2_2 + e1_1_3*e1_3_2*e2_2_1 +e1_2_1*e1_3_2*e2_1_3 - e1_2_1*e1_3_3*e2_1_2 - e1_2_2*e1_3_1*e2_1_3 + e1_2_2*e1_3_3*e2_1_1 +e1_2_3*e1_3_1*e2_1_2 - e1_2_3*e1_3_2*e2_1_1; 
CC = e1_1_1*e2_2_2*e2_3_3 - e1_1_1*e2_2_3*e2_3_2 - e1_1_2*e2_2_1*e2_3_3 + e1_1_2*e2_2_3*e2_3_1 +e1_1_3*e2_2_1*e2_3_2 - e1_1_3*e2_2_2*e2_3_1 - e1_2_1*e2_1_2*e2_3_3 + e1_2_1*e2_1_3*e2_3_2 +e1_2_2*e2_1_1*e2_3_3 - e1_2_2*e2_1_3*e2_3_1 - e1_2_3*e2_1_1*e2_3_2 + e1_2_3*e2_1_2*e2_3_1 +e1_3_1*e2_1_2*e2_2_3 - e1_3_1*e2_1_3*e2_2_2 - e1_3_2*e2_1_1*e2_2_3 + e1_3_2*e2_1_3*e2_2_1 +e1_3_3*e2_1_1*e2_2_2 - e1_3_3*e2_1_2*e2_2_1; 
DD = e2_1_1*e2_2_2*e2_3_3 - e2_1_1*e2_2_3*e2_3_2 - e2_1_2*e2_2_1*e2_3_3 + e2_1_2*e2_2_3*e2_3_1 +e2_1_3*e2_2_1*e2_3_2 - e2_1_3*e2_2_2*e2_3_1;

cp = [AA BB CC DD];


gg = cp ./ cp(1);

AA = gg(1);
BB = gg(2);
CC = gg(3);
DD = gg(4);
descri = 18 * AA * BB * CC * DD - 4 * (BB^3) * DD + (BB^2) * (CC^2) - 4 * AA * (CC^3) - 27 * (AA^2) * (DD^2);    

CM = [0 0  -gg(4);
      1 0  -gg(3);
      0 1  -gg(2)];

value = eig(CM);
counter = 1;
for i = 1:length(value)
    if abs(imag(value(i))) < 10e-6
        Fs{counter} = real(value(i)) * F1 + F2;
        counter = counter + 1;
    end
end

end
